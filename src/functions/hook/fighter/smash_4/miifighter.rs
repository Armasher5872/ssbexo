//Mii Brawler Once Per Fighter Frame
#[skyline::hook(offset = MIIFIGHTER_VTABLE_ONCE_PER_FIGHTER_FRAME)]
unsafe extern "C" fn miifighter_opff(vtable: u64, fighter: &mut Fighter) -> u64 {
    let boma = fighter.battle_object.module_accessor;
    let entry_id = WorkModule::get_int(boma, *FIGHTER_INSTANCE_WORK_ID_INT_ENTRY_ID) as usize;
    let status_kind = StatusModule::status_kind(boma);
    if StatusModule::situation_kind(boma) == *SITUATION_KIND_GROUND || [*FIGHTER_STATUS_KIND_DAMAGE, *FIGHTER_STATUS_KIND_DAMAGE_AIR, *FIGHTER_STATUS_KIND_DAMAGE_FLY, *FIGHTER_STATUS_KIND_DAMAGE_FLY_ROLL, *FIGHTER_STATUS_KIND_DAMAGE_FLY_METEOR, *FIGHTER_STATUS_KIND_DAMAGE_FLY_REFLECT_LR, *FIGHTER_STATUS_KIND_DAMAGE_FLY_REFLECT_U, *FIGHTER_STATUS_KIND_DAMAGE_FLY_REFLECT_D, *FIGHTER_STATUS_KIND_DAMAGE_FALL].contains(&status_kind) {
        USE_ONSLAUGHT[entry_id] = true;
    }
    if [*FIGHTER_STATUS_KIND_SPECIAL_S, *FIGHTER_MIIFIGHTER_STATUS_KIND_SPECIAL_S1_END].contains(&status_kind) {
        //fighter.sub_transition_group_check_air_cliff();
        USE_ONSLAUGHT[entry_id] = false;
    }
    /*
    if [*FIGHTER_MIIFIGHTER_STATUS_KIND_SPECIAL_LW2_START, *FIGHTER_MIIFIGHTER_STATUS_KIND_SPECIAL_LW2_KICK].contains(&status_kind) {
        fighter.sub_transition_group_check_air_cliff();
    }
    */
    original!()(vtable, fighter)
}

pub fn install() {
	skyline::install_hooks!(
        miifighter_start_initialization,
        miifighter_reset_initialization,
        miifighter_opff
    );
}